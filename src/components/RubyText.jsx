const tokenize = (text) => {
  const keys = Object.keys(rubyMap).sort((a, b) => b.length - a.length);
  const parts = [];
  let index = 0;

  while (index < text.length) {
    const match = keys.find((key) => text.startsWith(key, index));
    if (match) {
      parts.push({ text: match, ruby: rubyMap[match] });
      index += match.length;
    } else {
      parts.push({ text: text[index] });
      index += 1;
    }
  }

  return parts;
};

export default function RubyText({ children, className = "" }) {
  if (children == null) return null;
  const text = String(children);
  return (
    <span className={className}>
      {tokenize(text).map((part, index) =>
        part.ruby ? (
          <ruby key={`${part.text}-${index}`}>
            {part.text}
            <rt>{part.ruby}</rt>
          </ruby>
        ) : (
          <React.Fragment key={`${part.text}-${index}`}>{part.text}</React.Fragment>
        )
      )}
    </span>
  );
}
